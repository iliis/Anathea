bool
Point::collide(Rect const& r) const
{
	return r.collide(*this);
};
//------------------------------------------------------------------------------------------------
bool
Point::collide(Box   const& b) const
{return this->collide(Rect(b));}
//------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------
bool
Rect::collide(Rect const& r) const
{
	//die Untergrenze des einen Rechteckes muss unterhalb der Obergrenze des anderen Rechteckes sein
	return
	(r.pos.x <= this->pos.x + this->size.x && r.pos.x+r.size.x >= this->pos.x) &&
	(r.pos.y <= this->pos.y + this->size.y && r.pos.y+r.size.y >= this->pos.y);
};
